package com.example.cg24.recyclerview;

public class Data {

    String point;
    String date;

    public Data(String point) {
        this.point = point;
        this.date=date;

    }

    public String getPoint() {
        return point;
    }

    public String getDate() {
        return date;
    }

    public void setPoint(String point){
        this.point=point;
    }

    public void setDate(String date){
        this.date=date;
    }

}
